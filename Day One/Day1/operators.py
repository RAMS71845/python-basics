#Addition, Subtraction, Multiplication, Division
print("2+3+2 is", 2+3+2)
print("2-3-2 is", 2-3-2)
print("2*3*2 is", 2*3*2)
print("2/3/2 is", 2/3/2)

#Floor division
print("5//3 is", 5//3)
print("7//5 is", 7//5)

# Modulo
print("5%3 is", 5%3)
print("7%5 is", 7%5)

#A classic use of modulo is to check if a number is even or odd
print("Is 10 even?", 10%2==0)#True
print("Is 11 even?", 11%2==0)#False

#exponentiation
print("2**3 is", 2**3)

#Precedence of operators
print("2+3*4 is", 2+3*4) #14 (not 20)
print("(2+3)*4 is", (2+3)*4) #20 (not 14)